#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#define SIZE 10000
#define INF 99999999

int stack[SIZE];
int top = -1; //스택의 최상단

void push(int data) {
    if (top == SIZE -1) {
        printf("스택 오버플로우가 발생했습니다. \n");
        return;
    }
    // 그렇지 않으면
    stack[++top] = data;
}

//데이터를 뽑아내는 함수
int pop() {
    // 원소가 하나도 없는 상태
    if (top == -1) {
        printf("스택 언더플로우가 발생했습니다. \n");
        return;
    }
    return stack[top--];
}

void show() {
    printf("--- 스택의 최상단 ---\n");
    for (int i = top; i>=0; i--) {
        printf("%d\n", stack[i]);
    }
    printf("--- 스택의 최하단 ---\n");
}

int main(void) {
    push(7);
    push(5);
    push(4);
    pop();
    push(6);
    pop();
    show();
    system("pause");
    return 0;
}